package javaImportantPrograms;

import java.util.Arrays;

public class char_sorting {

	public static void main(String[] args) {
     char ch[]= {'Z','D','M','G','A'};
     System.out.println("Array Before Sorting: "+Arrays.toString(ch));
     Arrays.sort(ch);
     System.out.println("Arrays After Sorting: "+Arrays.toString(ch));
     }

}
